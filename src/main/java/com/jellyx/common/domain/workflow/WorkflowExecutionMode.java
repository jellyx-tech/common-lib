package com.jellyx.common.domain.workflow;

/**
 * Режимы выполнения воркфлоу.
 * Определяет, как воркфлоу должен реагировать на входящие события.
 */
public enum WorkflowExecutionMode {
    
    /**
     * Постоянный режим - воркфлоу выполняется при каждом срабатывании триггера.
     * Воркфлоу остается активным до тех пор, пока не будет отключен пользователем.
     */
    CONTINUOUS,
    
    /**
     * Одноразовый режим - воркфлоу ждет первое событие в течение заданного времени,
     * выполняет воркфлоу один раз и затем отключается.
     * Если событие не приходит в течение таймаута, воркфлоу отключается без выполнения.
     */
    ONE_TIME
}
